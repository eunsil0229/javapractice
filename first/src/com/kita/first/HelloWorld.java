package com.kita.first; // 지우면 안 됨

public class HelloWorld { // 클래스 -> 객체를 만들 수 있게 함
	// 프로그램 실행을 하려면 메인 메소드가 있어야 함
	
	public static void main(String[] args) { // 정의, 한 클래스 안에 한 메인 메소드만 가능
		System.out.println("헬로우 월드!!"); // 이미 만들어진 메소드를 호출
		// "큰 따옴표" = 문자열
		System.out.println("헬로우 월드!!"); // ln = line = 개행 -> 문자를 다 출력한 후 엔터를 쳐줌
		System.out.print("헬로우 월드!!");
		System.out.print("헬로우\n"); // 문자열 안에 \n -> 개행
		System.out.print("월드!!");
		
		/*
		 주석
		 */
	}

}

